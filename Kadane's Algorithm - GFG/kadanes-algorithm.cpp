class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        int max_till_now=*max_element(arr,arr+n);
        int curr=0;
        for(int i=0;i<n;i++){
            curr=curr+arr[i];
            max_till_now=max(max_till_now,curr);
            if(curr<0){
                curr=0;
            }
        }
        return max_till_now;
        
    }
};

